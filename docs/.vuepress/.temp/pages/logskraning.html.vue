<p>Skráningu áritana erlendra ríkisborgara á atvinnuréttindi þeirra til staðfestingar um að þeir hafi heimild til þess að gegna tilteknum stöðum um borð í íslenskum skipum. Þessir erlendu ríkisborgarar geta verið með íslenska kennitölu en einnig  með „dummy“-kennitölu sakir þess að þeir starfa á íslenskum skipum sem gerð eru út af erlendum útgerðum.</p>
<p>Kerfið skal hannað þannig að það kanni sjálft hvort skilyrði skírteinisútgáfu, hvort heldur er vegna frumútgáfu atvinnuréttinda eða endurnýjunar sé fullnægt. M.ö.o. að kerfið útbúi, þegar um er að ræða umsóknir um útgáfu eða endurnýjun skírteina, matsblað þar sem kerfið hakar við þau skilyrði sem telast fullnægt en tilgreinir athugasemdir ef einhverjum skilyrða er ekki fullnægt. Þessi hluti kerfisins er mikilvægur þar sem hann auðveldar til muna alla afgreiðslu umsókna um útgáfu atvinnuskírteina. Í dag er gátun skilyrða fyrir útgáfu atvinnuskírteina handvirk. Það er ekkert til fyrirstöðu því að slík gátun fari fram sjálfvirkt þegar að viðurkenndir læknar hafa tengst kerfinu og útgáfa læknisvottorða fer fram í kerfinu eða þegar að önnur læknisvottorð hafa verið færð inn í kerfið.</p>
