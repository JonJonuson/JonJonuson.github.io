<p><code v-pre>LÖGSKRÁNINGAR</code>  →   <a href="https://skutantest.samgongustofa.is/ships/20005/registrationExemption" target="_blank" rel="noopener noreferrer">Undanþágur <ExternalLinkIcon/></a></p>
<p>Skráðar undanþágur til þess að gegna stöðu á skipi sem viðkomandi hefur ekki rétt til að gegna</p>
<p>Í undanþágu fellst að veita tilteknum einstaklingi heimild til þess að gegna stöðu sem hann hefur ekki réttindi til að gegna á tilteknu skipi í tiltekinn tíma. Undanþágan beinist að einstaklingnum og réttindum hans.</p>
<p>Það er útgerð skips sem sækir um undanþágu til að tiltekninn sjómaður megi gegna stöðu um borð</p>
<p>Nánar er fjallað um skráningar undanþágu í kaflanum um lögskráningar (sjá kafla 5.4).</p>
<p>Í þessari aðgerð er í upphafi birtar allar skáðar undanþágur fyrir viðkomandi skip, en hægt er að velja tímabil til að sía hvað er birt í listanum.</p>
<p>Hægt er að velja ákveðna undanþágu til að skoða nánar eða skrá nýja ef viðkomandi hefur réttindi til þess.</p>
<h2 id="atvinnurettindi" tabindex="-1"><a class="header-anchor" href="#atvinnurettindi" aria-hidden="true">#</a> Atvinnuréttindi</h2>
<p>(skírteini sjómanna) eru veitt til sjómanna þegar ákveðnum skilyrðum er fullnægt. Bak við hver réttindi er mat (matsblað) þar sem tilgreint er hvað viðkomandi sjómaður þarf að klára áður en hann getur fengið viðkomandi réttindi. Þau atriði eru t.d. að viðkomandi hafi verið í tilgreindu námi og staðist próf, setið námskeið, hafa náð ákveðnum fjölda siglingartíma í ákveðinni stöðu, staðist læknisskoðun (vottorð veitt) o.s.frv.</p>
<p>Mismunandi atvinnuréttindi krefjast mismunandi forkrafna, en þau eru öll af svipuðu meiði.</p>
<ul>
<li>
<p><strong>Aldur sjómanns</strong>  <br/>Reiknað út frá kennitölu sjómanns eða skráningu fæðingardags þegar um er að ræða erlendan sjóman.</p>
</li>
<li>
<p><strong>Menntun</strong> <br/>Sjómaður þarf að hafa klárað ákveðið nám. Sjá nánar tegundir menntunar í kafla 7.2.</p>
</li>
<li>
<p><strong>Siglingartími</strong> <br/>Útreiknað út frá gögnum skráðum við lögskráningar í kerfinu.</p>
</li>
<li>
<p><strong>Námskeið</strong> <br/> Sjómaður þarf að hafa klárað ákveðin námskeið. Sjá nánar tegundir námskeiða í kafla 7.2.</p>
</li>
<li>
<p><strong>Heilbrigðiskröfur</strong> <br/>Sjómaður þarf að hafa staðist ákveðnar læknisskoðanir. Sjá nánar um læknisskoðanir í kafla 4.5.</p>
</li>
</ul>
<details class="custom-container details"><summary>Nánar um atvinnuréttindi og útgáfu skírteina</summary>
<p>Þegar gefin eru út skírteini með atvinnuréttindum þarf að taka tillit til prófa, og prófgráða sjómanns, sem og siglingatíma og skiptir máli hvað stöðum sjómaður hefur gegnt, hve lengi, á hvaða stærð skips og á hvaða stærð aðalvélar. Einnig er krafa um að sjómaður hafi sótt ýmis námskeið til þess að fá útgefin fyrstu réttindi og síðan önnur námskeið við endurnýjun réttindi, til dæmis öryggisfræðslunámskeið.</p>
<p>Í kafla 6.2 er fjallað um þau atvinnuréttindi sem hægt er að skrá í kerfinu og hvað þarf að liggja til grundvallar fyrir hvert og eitt mat(“matsblað”).</p>
<p>Við skráningu á kerfið á að leiðbeina og aðstoða þannig að upp komi “matsblað” fyrir viðkomandi umsókn og skal kerfið sækja viðeigandi upplýsingar úr kerfinu sbr. Siglingartíma, námskeið, próf, prófgráður og annað sem tengist viðkomandi atvinnuskírteini.</p>
<p>Gerandi skal þá geta skoðað siglingatíma sjómanns sundurliðað.</p>
<p>Gerandi skráir fyrst inn atvinnuskírteini á sjómann. Síðan eru skráð réttindi á atvinnuskírteinið. Ákveðin réttindi eru tengd hverju atvinnuskírteini þannig að notandi velur úr þessum réttindum þegar hann er að skrá á skírteinið. Hægt er að skrá mörg atvinnuréttindi á eitt skírteini.</p>
<p>Við skráningu á atvinnuréttindum skal vista hvað var lagt til grundvallar þannig að hægt sé sýna fram á að öllum viðeigandi skilyrðum við útgáfu
réttindakskírteinis hafi verið fullnægt á þeim tíma sem þau voru gefin út.</p>
<p>Venjulega fer hámarks gildistími skírteinis eftir skírteinistegund (oftast 5 ár) en gerandi skal geta fært inn á skírteinið skemmri gildistíma ef þörf er á.</p>
<p>Hægt þarf að vera að lista út öll útgefin atvinnuréttindi fyrir viðkomandi sjómann og birta afrit af skírteininu þegar það á við.</p>
<p>Umsækjandi skal geta valið um hvort hann fær útgefin öll atvinnuéttindi fyrir tilteknar gerðir skipa í einu skírteini eða fleirum.</p>
<p>Ef margar gerðir atvinnuréttinda eru færð inn á sama skírteni skal gildistími sem færður er á skírteinið vera sá gildistími sem stystur er.</p>
<p>Við útprentun á atvinnuskírteinum er notast við sniðmát fyrir viðkomandi gerð skírteinis, sjá nánar í kafla 7.14.</p>
<p>Þegar sjómaður við endurnýjun atvinuskírteinis fullnægir ekki skilyrðum um að hafa tiltekinn siglingatíma að baki, á hann þess kost að fá útgefið skírteini með réttindi til að gegna næstu lægri stöðu en hann á að öðrum kosti rétt til. Við endurnýjun atvinnuskírteinis skal kerfið staðfesta hvort skilyrðum  um siglingatíma til viðhalds atvinuréttindum sé fullnægt. Sé skilyrðum um siglingatími ekki fullnægt skal kerfið tilgreina þá næstu lægri stöðu sem umsækjandi á rétt til að fá réttindi til að gegna. Við útgáfu þess skírteinis skal koma fram athugasemd um að réttindi sjómanns séu skert af þessum sökum til að koma í veg fyrir að sjómaður fái ítrekað skerðingu á þeim réttindum sem hann  hafði aflað sér.
Atvinnuréttidi sem gefin eru út til samræmis við ákvæði STCW samþykktarinnar má ekki gefa út á skírteini sem gilda um fiskiskip, varðskip, skemmtibáta og önnur skip og öfugt.
Það getur komið fyrir að atvinnuréttindi eru dæmt af sjómönnum (réttindum svipt). Í þeim tilvikum þarf að verka hægt að merkja skírteinið þannig og setja tilvísun í málið í skjalastjórnunarkerfi.</p>
