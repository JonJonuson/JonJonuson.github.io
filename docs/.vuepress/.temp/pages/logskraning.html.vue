<p>er þegar samþykkt hefur verið frávik frá þeirri mönnunarreglu sem lög og reglur mæla fyrir um sem og þegar Samgöngustofa úrskurðar um mönnun skips, hvort heldur tímabundið eða ótímabundið. Slíkar sérreglur eru að jafnaði háð tilteknum skilyrðum. Athugið að hér er átt við hvaða réttindi og stöður áhöfn þarf að uppfylla á viðkomandi skipi til þess að fullnægja skilyrðum laga.</p>
<p>Einnig eru til <strong>undanþágur</strong> en þá er átt við undanþágu sem veitt er til eins ákveðins einstaklings til að hann megi gegna ákveðinni stöðu um borð í tilteknu skipi í tiltekinn tíma.</p>
